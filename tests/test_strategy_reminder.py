@pytest.mark.unit
class TestBuildReminderText:
    def test_returns_none_when_strategy_empty(self) -> None:
        assert build_reminder_text([]) is None

    def test_lists_section_titles_only(self) -> None:
        entries = [
            StrategyEntry(context_type="goal", title="Q2 CPA target", content="..."),
            StrategyEntry(context_type="persona", title="B2B SaaS", content="..."),
        ]
        text = build_reminder_text(entries)
        assert text is not None
        assert "Q2 CPA target" in text
        assert "B2B SaaS" in text
        # Content must NOT appear — only titles. The dummy content is
        # ``"..."`` which is also a natural token in prose, but every
        # entry uses the same content so its absence (zero occurrences)
        # pins that no entry's content was rendered.
        assert text.count("...") == 0

    def test_marks_as_reminder(self) -> None:
        entries = [StrategyEntry(context_type="goal", title="g1", content="x")]
        text = build_reminder_text(entries)
        assert text is not None
        assert "STRATEGY" in text.upper() or "reminder" in text.lower()

    def test_caps_at_reasonable_section_count(self) -> None:
        """Defensive: a STRATEGY.md with 100 sections should not balloon
        the reminder. Cap at a sensible number (around 20)."""
        entries = [
            StrategyEntry(context_type="t", title=f"title_{i}", content="x")
            for i in range(100)
        ]
        text = build_reminder_text(entries)
        assert text is not None
        # First 20 must be present.
        assert "title_0" in text
        assert "title_19" in text
        # Truncated entries must NOT appear, AND the explicit "+N more"
        # indicator must be present so the cap is observable to the
        # agent. Both halves pinned separately (round-2 review caught
        # that the v1 ``or`` masked regressions where one half silently
        # dropped).
        assert "title_20" not in text
        assert "title_99" not in text
        assert "+80 more" in text


