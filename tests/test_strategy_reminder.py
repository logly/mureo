def _all_builtin_tool_names() -> set[str]:
    """Discover every built-in tool name at runtime by walking the
    server's registered TOOLS lists. The truth-table must match this
    set exactly (modulo the plugin namespace, which is out of scope)."""
    from mureo.mcp.server import (
        _ANALYSIS_NAMES,
        _ANALYTICS_REGISTRY_NAMES,
        _GOOGLE_ADS_NAMES,
        _LEARNING_NAMES,
        _META_ADS_NAMES,
        _MUREO_CONTEXT_NAMES,
        _ROLLBACK_NAMES,
        _SEARCH_CONSOLE_NAMES,
    )

    return (
        set(_GOOGLE_ADS_NAMES)
        | set(_META_ADS_NAMES)
        | set(_SEARCH_CONSOLE_NAMES)
        | set(_ROLLBACK_NAMES)
        | set(_ANALYSIS_NAMES)
        | set(_MUREO_CONTEXT_NAMES)
        | set(_ANALYTICS_REGISTRY_NAMES)
        | set(_LEARNING_NAMES)
    )


@pytest.mark.unit
class TestFullCatalogueTruthTable:
    """Pin the classifier against the FULL built-in tool registry, not
    just a hand-picked sample. A new built-in tool added without a
    classifier-set update fails CI here."""

    def test_classifier_matches_truth_table_for_every_builtin_tool(self) -> None:
        all_names = _all_builtin_tool_names()
        mismatches: list[str] = []
        for name in sorted(all_names):
            expected_mutating = name in _MUTATING_CATALOGUE
            actual_mutating = is_mutating_builtin_tool(name)
            if expected_mutating != actual_mutating:
                mismatches.append(
                    f"  {name}: truth={expected_mutating} "
                    f"classifier={actual_mutating}"
                )
        assert not mismatches, (
            "Classifier disagrees with truth table on the following "
            "tools — either fix _MUTATING_BUILTIN_SUFFIXES / _EXACT_NAMES "
            "in strategy_reminder.py or update _MUTATING_CATALOGUE "
            "above:\n" + "\n".join(mismatches)
        )

    def test_truth_table_contains_only_real_tool_names(self) -> None:
        """The other direction: every name in the truth table must
        correspond to a real registered tool. Catches stale entries
        after a tool rename or removal."""
        all_names = _all_builtin_tool_names()
        stale = sorted(_MUTATING_CATALOGUE - all_names)
        assert not stale, (
            "Truth table references tool names that no longer exist in "
            "the registry — remove from _MUTATING_CATALOGUE: " + ", ".join(stale)
        )


