"""Tests for the strategy-reminder injection (v0.9.24).

When the dispatcher runs a built-in mutating MCP tool, it appends a
short reminder TextContent block to the tool's response listing the
section titles from STRATEGY.md so the agent re-surfaces the strategy
context after every mutation. Soft enforcement only — never refuses,
just injects.

These tests pin:

1. The reminder fires only for *mutating* built-in tools (not for
   read-only / discover tools, not for plugin tools — those are out of
   scope for v1).
2. The reminder is *appended*, never replaces the tool's content.
3. Missing STRATEGY.md silently skips the reminder.
4. Env-var opt-out (``MUREO_DISABLE_STRATEGY_REMINDER=1``) suppresses
   the reminder entirely.
5. The reminder lists STRATEGY.md section titles only — never the
   content (small context cost, prevents bloat).
"""

