@pytest.mark.unit
@pytest.mark.asyncio
class TestDispatcherInjection:
    async def _run(
        self,
        tool_name: str,
        handler_result: list[Any],
        strategy: list[StrategyEntry] | None = None,
        env: dict[str, str] | None = None,
    ) -> list[Any]:
        from mureo.mcp.server import handle_call_tool

        fake_ctx = MagicMock()
        fake_ctx.state_store.read_strategy.return_value = (
            strategy if strategy is not None else []
        )
        fake_handler = AsyncMock(return_value=handler_result)
        # Route every test through a single real handler branch
        # (rollback) so we exercise the dispatcher integration without
        # building Google Ads / Meta Ads mocks per case.
        with (
            patch("mureo.mcp.server._load_policy_gates", return_value=()),
            patch("mureo.mcp.server.handle_rollback_tool", new=fake_handler),
            patch(
                "mureo.mcp.server._ROLLBACK_NAMES",
                new={tool_name},
            ),
            patch(
                "mureo.core.strategy_reminder.get_runtime_context",
                return_value=fake_ctx,
            ),
            patch.dict(
                "os.environ",
                env if env is not None else {},
                clear=False,
            ),
        ):
            if env is not None and "MUREO_DISABLE_STRATEGY_REMINDER" not in env:
                # Make sure the opt-out env var is NOT inherited from
                # the developer's shell — otherwise the suite fails
                # depending on who runs it.
                import os

                os.environ.pop("MUREO_DISABLE_STRATEGY_REMINDER", None)
            return await handle_call_tool(tool_name, {})

    async def test_mutating_tool_with_strategy_gets_reminder_appended(self) -> None:
        result = await self._run(
            tool_name="_synthetic_test_tool_create",
            handler_result=[TextContent(type="text", text="original tool output")],
            strategy=[
                StrategyEntry(context_type="goal", title="Q2 CPA target", content="x"),
            ],
        )
        # Original content preserved + reminder appended.
        assert len(result) == 2
        assert result[0].text == "original tool output"
        assert "Q2 CPA target" in result[1].text

    async def test_read_only_tool_gets_no_reminder(self) -> None:
        result = await self._run(
            tool_name="_synthetic_test_tool_get",
            handler_result=[TextContent(type="text", text="original tool output")],
            strategy=[
                StrategyEntry(context_type="goal", title="Q2 CPA target", content="x"),
            ],
        )
        assert len(result) == 1
        assert result[0].text == "original tool output"

    async def test_missing_strategy_silently_skips(self) -> None:
        result = await self._run(
            tool_name="_synthetic_test_tool_create",
            handler_result=[TextContent(type="text", text="original tool output")],
            strategy=[],
        )
        assert len(result) == 1
        assert result[0].text == "original tool output"

    async def test_env_var_opt_out_suppresses_reminder(self) -> None:
        result = await self._run(
            tool_name="_synthetic_test_tool_create",
            handler_result=[TextContent(type="text", text="original tool output")],
            strategy=[
                StrategyEntry(context_type="goal", title="Q2 CPA target", content="x"),
            ],
            env={"MUREO_DISABLE_STRATEGY_REMINDER": "1"},
        )
        assert len(result) == 1
        assert result[0].text == "original tool output"

    @pytest.mark.parametrize(
        "val",
        ["true", "yes", "True", "TRUE", "0", " 1", "1 ", "", "01", "11"],
        ids=[
            "true",
            "yes",
            "True",
            "TRUE",
            "zero",
            "leading_space",
            "trailing_space",
            "empty",
            "01",
            "11",
        ],
    )
    async def test_non_exact_one_does_not_opt_out(self, val: str) -> None:
        """The opt-out env var follows the established mureo contract:
        exact-string ``"1"`` only. ``"true"`` / ``"yes"`` / ``" 1"``
        / ``""`` / etc. must NOT disable the reminder. Matches the
        `_is_disabled` contract pinned in test_truthy_coercion_does_
        not_disable for ``MUREO_DISABLE_GOOGLE_ADS`` et al."""
        result = await self._run(
            tool_name="_synthetic_test_tool_create",
            handler_result=[TextContent(type="text", text="original tool output")],
            strategy=[
                StrategyEntry(context_type="goal", title="Q2 CPA target", content="x"),
            ],
            env={"MUREO_DISABLE_STRATEGY_REMINDER": val},
        )
        # Reminder STILL fires because val != exact "1".
        assert len(result) == 2
        assert "Q2 CPA target" in result[1].text

    async def test_state_read_failure_does_not_break_dispatch(self) -> None:
        """A corrupted STATE.json / missing strategy file MUST NOT
        break the mutating tool dispatch — the reminder is best-
        effort and silent on failure."""
        from mureo.mcp.server import handle_call_tool

        fake_ctx = MagicMock()
        fake_ctx.state_store.read_strategy.side_effect = OSError("disk gone")
        fake_handler = AsyncMock(
            return_value=[TextContent(type="text", text="original")]
        )
        with (
            patch("mureo.mcp.server._load_policy_gates", return_value=()),
            patch("mureo.mcp.server.handle_rollback_tool", new=fake_handler),
            patch(
                "mureo.mcp.server._ROLLBACK_NAMES",
                new={"_synthetic_test_tool_create"},
            ),
            patch(
                "mureo.core.strategy_reminder.get_runtime_context",
                return_value=fake_ctx,
            ),
        ):
            result = await handle_call_tool("_synthetic_test_tool_create", {})
        # Original output preserved; no reminder; no exception.
        assert len(result) == 1
        assert result[0].text == "original"
